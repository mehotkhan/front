<script setup lang="ts">
import Placeholder from "@tiptap/extension-placeholder";
import StarterKit from "@tiptap/starter-kit";
import {
  BubbleMenu,
  EditorContent,
  FloatingMenu,
  useEditor,
} from "@tiptap/vue-3";
import AutoJoiner from "tiptap-extension-auto-joiner";
import { Markdown } from "tiptap-markdown";
import { onBeforeUnmount, unref } from "vue";

import GlobalDragHandle from "../editor/dragger";
import { getExtensions } from "../editor/Extension";

// Retrieve any custom extensions.
const customExtensions = await getExtensions();

const props = defineProps({
  body: { type: String, required: true, default: "" },
});
const { t } = useI18n();
const emit = defineEmits<{ (e: "update", value: string): void }>();

// Create the editor instance directly.
const editor = useEditor({
  content: props.body,
  extensions: [
    StarterKit,
    FloatingMenu,
    BubbleMenu,
    GlobalDragHandle.configure({
      dragHandleWidth: 50,
      scrollTreshold: 500,
      excludedTags: [],
      customNodes: [],
    }),
    ...customExtensions,
    Markdown,
    Placeholder.configure({ placeholder: t("Type Something...") }),
    AutoJoiner,
  ],
  onUpdate({ editor }) {
    emit("update", editor.storage.markdown.getMarkdown());
  },
});

// Cleanup: destroy the editor when the component unmounts.
onBeforeUnmount(() => {
  unref(editor)?.destroy();
});
</script>

<template>
  <div v-if="editor" class="relative">
    <USeparator class="mt-5" />

    <!-- Floating Menu -->
    <FloatingMenu :editor="editor" :tippy-options="{ duration: 100 }">
      <UButtonGroup
        class="flex justify-center rounded-none bg-gray-100 dark:bg-slate-800 p-1"
        size="md"
      >
        <UTooltip :text="$t('Horizontal Line')">
          <UButton
            icon="i-lucide-minus"
            variant="link"
            @click="editor.chain().focus().setHorizontalRule().run()"
          />
        </UTooltip>
        <UTooltip :text="$t('Blockquote')">
          <UButton
            icon="i-lucide-quote"
            variant="link"
            @click="editor.chain().focus().toggleBlockquote().run()"
          />
        </UTooltip>
        <UTooltip :text="$t('Paragraph')">
          <UButton
            :class="{ 'is-active': editor.isActive('paragraph') }"
            icon="i-lucide-pilcrow"
            variant="link"
            @click="editor.chain().focus().setParagraph().run()"
          />
        </UTooltip>
        <UTooltip :text="$t('Heading 1')">
          <UButton
            :class="{ 'is-active': editor.isActive('heading', { level: 1 }) }"
            icon="i-lucide-heading-1"
            variant="link"
            @click="editor.chain().focus().toggleHeading({ level: 1 }).run()"
          />
        </UTooltip>
        <UTooltip :text="$t('Heading 2')">
          <UButton
            :class="{ 'is-active': editor.isActive('heading', { level: 2 }) }"
            icon="i-lucide-heading-2"
            variant="link"
            @click="editor.chain().focus().toggleHeading({ level: 2 }).run()"
          />
        </UTooltip>
        <UTooltip :text="$t('Heading 3')">
          <UButton
            :class="{ 'is-active': editor.isActive('heading', { level: 3 }) }"
            icon="i-lucide-heading-3"
            variant="link"
            @click="editor.chain().focus().toggleHeading({ level: 3 }).run()"
          />
        </UTooltip>
        <UTooltip :text="$t('Bullet List')">
          <UButton
            :class="{ 'is-active': editor.isActive('bulletList') }"
            icon="i-lucide-list"
            variant="link"
            @click="editor.chain().focus().toggleBulletList().run()"
          />
        </UTooltip>
        <UTooltip :text="$t('Ordered List')">
          <UButton
            :class="{ 'is-active': editor.isActive('orderedList') }"
            icon="i-lucide-list-ordered"
            variant="link"
            @click="editor.chain().focus().toggleOrderedList().run()"
          />
        </UTooltip>
      </UButtonGroup>
    </FloatingMenu>

    <!-- Bubble Menu -->
    <BubbleMenu :editor="editor" :tippy-options="{ duration: 100 }">
      <UButtonGroup
        class="flex justify-center rounded-none bg-gray-100 dark:bg-slate-800 p-1"
        size="md"
      >
        <UTooltip :text="$t('Blockquote')">
          <UButton
            icon="i-lucide-quote"
            variant="link"
            @click="editor.chain().focus().toggleBlockquote().run()"
          />
        </UTooltip>
        <UTooltip :text="$t('Bold')">
          <UButton
            :disabled="!editor.can().chain().focus().toggleBold().run()"
            :class="{ 'is-active': editor.isActive('bold') }"
            icon="i-lucide-bold"
            variant="link"
            @click="editor.chain().focus().toggleBold().run()"
          />
        </UTooltip>
        <UTooltip :text="$t('Italic')">
          <UButton
            :disabled="!editor.can().chain().focus().toggleItalic().run()"
            :class="{ 'is-active': editor.isActive('italic') }"
            icon="i-lucide-italic"
            variant="link"
            @click="editor.chain().focus().toggleItalic().run()"
          />
        </UTooltip>
        <UTooltip :text="$t('Strikethrough')">
          <UButton
            :disabled="!editor.can().chain().focus().toggleStrike().run()"
            :class="{ 'is-active': editor.isActive('strike') }"
            icon="i-lucide-strikethrough"
            variant="link"
            @click="editor.chain().focus().toggleStrike().run()"
          />
        </UTooltip>
        <UTooltip :text="$t('Paragraph')">
          <UButton
            :class="{ 'is-active': editor.isActive('paragraph') }"
            icon="i-lucide-pilcrow"
            variant="link"
            @click="editor.chain().focus().setParagraph().run()"
          />
        </UTooltip>
        <UTooltip :text="$t('Heading 1')">
          <UButton
            :class="{ 'is-active': editor.isActive('heading', { level: 1 }) }"
            icon="i-lucide-heading-1"
            variant="link"
            @click="editor.chain().focus().toggleHeading({ level: 1 }).run()"
          />
        </UTooltip>
        <UTooltip :text="$t('Heading 2')">
          <UButton
            :class="{ 'is-active': editor.isActive('heading', { level: 2 }) }"
            icon="i-lucide-heading-2"
            variant="link"
            @click="editor.chain().focus().toggleHeading({ level: 2 }).run()"
          />
        </UTooltip>
        <UTooltip :text="$t('Heading 3')">
          <UButton
            :class="{ 'is-active': editor.isActive('heading', { level: 3 }) }"
            icon="i-lucide-heading-3"
            variant="link"
            @click="editor.chain().focus().toggleHeading({ level: 3 }).run()"
          />
        </UTooltip>
      </UButtonGroup>
    </BubbleMenu>

    <!-- Editor Content -->
    <EditorContent :editor="editor" class="min-h-[30rem] mt-5" />
  </div>
</template>

